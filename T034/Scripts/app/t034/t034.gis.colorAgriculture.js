// возвращает L.Icon по наименованю сх культуры
function getIconByAgriculture(agriculture) {

    var arr = jQuery.grep(iconAgriculture, function (item, index) {
        return (item.agriculture === agriculture);
    });

    if (arr.length > 0)
        return arr[0].icon;
    else {
        return new L.Icon();
    }
}
